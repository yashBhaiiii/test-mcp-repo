# a. Usage of methods such as floor(), ceil(), sqrt(), isqrt(), gcd() etc. 

import math

num1 = 3.453
num2 = 64

print("\nP.1. a) Math Library methods: ")
print(f"\n-> floor of {num1} is: {math.floor(num1)}")
print(f"-> ceil of {num1} is: {math.ceil(num1)}")
print(f"-> sqrt of {num2} is: {math.sqrt(num2)}")
print(f"-> isqrt of {num2} is: {math.isqrt(num2)}")
